CREATE DATABASE sales_performance;
USE sales_performance;
CREATE TABLE departments (
	dept_id INT PRIMARY KEY,
    dept_name VARCHAR(50) NOT NULL
);

CREATE TABLE employees (
	emp_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    dept_id INT,
    salary DECIMAL(10,2),
    manager_id INT,
    joining_date DATE,
    CONSTRAINT fk_employees FOREIGN KEY employees(dept_id) REFERENCES departments(dept_id) ON DELETE SET NULL
 );

CREATE TABLE orders (
	order_id INT PRIMARY KEY,
    emp_id INT NOT NULL,
    order_amount DECIMAL(10,2),
    order_date DATE,
    CONSTRAINT fk_orders FOREIGN KEY orders(emp_id) REFERENCES employees(emp_id) ON DELETE CASCADE
 );




