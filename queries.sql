-- 9. Departments with 3 consecutive negative growth months
-- Business Insight: identifies department with negative growth
WITH DeptMonthlyRev AS (
    select d.dept_id, d.dept_name, DATE_FORMAT(o.order_date, '%Y-%m-01') AS month_start,
        SUM(o.order_amount) AS revenue
    from departments d
    join employees e on d.dept_id = e.dept_id
    join orders o on e.emp_id = o.emp_id
    group by d.dept_id, d.dept_name, month_start
),

GrowthCalc as (
    select dept_id, dept_name, month_start, revenue,
        lag(revenue) over (partition by dept_id 
							order by month_start) as prev_revenue,
        revenue - lag(revenue) over (partition by dept_id 
									 order by month_start) as growth
    from DeptMonthlyRev
),

Streaks as (
    select
        *,
        CASE when growth < 0 then 1 else 0 end as is_negative,
        row_number() over (
            partition by dept_id 
            order by month_start
        ) 
        -
        row_number() over (
            partition by dept_id, 
            case when growth < 0 then 1 else 0 end
            order by month_start
        ) as grp
    from GrowthCalc
)

select dept_id from Streaks where is_negative = 1
group by dept_id, dept_name, grp
having COUNT(*) >= 3;





-- 10. Rolling 3-month average revenue per department
-- Business Insight: Average revenue generated by department in past 3 months

-- This is a strict 3-Month Average
with DeptMonthlyRevenue as (
		select d.dept_id, d.dept_name, date_format(o.order_date, '%Y-%m') as YearMonth,
		sum(o.order_amount) as Revenue
		from departments d
		join employees e on d.dept_id = e.dept_id
		join orders o on e.emp_id = o.emp_id
		group by
		d.dept_id, d.dept_name, date_format(o.order_date, '%Y-%m')
)
select dept_id, dept_name, YearMonth, Revenue,
case 
	when count(*) over (partition by dept_id order by YearMonth
						rows between 2 preceding and current row) = 3
	then avg(Revenue) over (partition by dept_id order by YearMonth
							rows between 2 preceding and current row)
	else null
end as rolling_3_month_avg 
from DeptMonthlyRevenue
order by dept_id, YearMonth;


-- this is not strict 
with DeptMonthlyRevenue as (
		select d.dept_id, d.dept_name, date_format(o.order_date, '%Y-%m') as YearMonth,
		sum(o.order_amount) as Revenue
		from departments d
		join employees e on d.dept_id = e.dept_id
		join orders o on e.emp_id = o.emp_id
		group by
		d.dept_id, d.dept_name, date_format(o.order_date, '%Y-%m')
)
select dept_id, dept_name, YearMonth, revenue, avg(revenue) over(partition by dept_id
											 order by YearMonth
                                             rows between 2 preceding and current row) as avg_3months
from DeptMonthlyRevenue
order by dept_id, YearMonth;
