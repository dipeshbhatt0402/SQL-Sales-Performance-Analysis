-- 1. Top 3 employees per department by revenue
-- Business Insight: Identifies top performers in each team
with EmployeesRevenue as (
		select employees.emp_id as emp_id, employees.name as name, employees.dept_id as dept_id, 
        sum(orders.order_amount) as revenue
		from employees
		inner join orders on employees.emp_id = orders.emp_id
		group by employees.emp_id, employees.name, employees.dept_id),
	-- for ranking of employees in a department
	EmployeesRanking as (
		select *, row_number() over(partition by dept_id order by revenue desc) as ranking
		from EmployeesRevenue)
select * 
from EmployeesRanking
where ranking < 4;




-- 2.  Revenue per department per month
-- Business Insight: Revenue Generated by department per month(contribution to the company)

-- this is by using aggregate function and group by clause
select employees.dept_id, date_format(orders.order_date, '%Y-%m') as YearMonth,
sum(orders.order_amount) as RevenuePerDepartment
from employees
inner join orders on employees.emp_id = orders.emp_id
-- where date_format(orders.order_date, '%Y-%m') is Not null
group by employees.dept_id, date_format(orders.order_date, '%Y-%m')
order by employees.dept_id, date_format(orders.order_date, '%Y-%m') asc;


-- this is by using window function which is using distinct
select distinct employees.dept_id, month(orders.order_date) as Month,
sum(orders.order_amount) over(partition by employees.dept_id, month(orders.order_date)
								order by employees.dept_id asc) as revenue_per_month
from employees
inner join orders on employees.emp_id = orders.emp_id;


 


