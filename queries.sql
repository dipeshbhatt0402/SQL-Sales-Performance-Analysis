-- 7. Department with negative growth 
-- Business Insight: Identifies the Team which shows negative growth compare to previous month
with MonthRevenue as (
	select  employees.dept_id as dept_id, date_format(orders.order_date, '%Y-%m') as order_date, sum(order_amount) as CurrentMonthRevenue
	from employees 
	inner join orders on employees.emp_id = orders.emp_id
	group by employees.dept_id, date_format(orders.order_date, '%Y-%m')
	order by employees.dept_id asc, date_format(orders.order_date, '%Y-%m') asc
),
MonthGrowth as (
	select dept_id, order_date, CurrentMonthRevenue, lag(CurrentMonthRevenue) over (partition by dept_id order by order_date)
		as PreviousMonthRevenue,
		CurrentMonthRevenue - lag(CurrentMonthRevenue) over (partition by dept_id order by order_date)
		as Growth from MonthRevenue
)
select * from MonthGrowth where Growth < 0;





