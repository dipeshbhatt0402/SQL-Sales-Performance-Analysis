-- 4. Reveneue Contribution % of each employee in department
-- Business Insight: Identifies Employees' contribution towards company

-- This is done using Aggregate function and group by clause no need of distinct
with EmpInfo as (
		select employees.emp_id as emp_id, employees.name as name, employees.dept_id as dept_id, 
		sum(orders.order_amount) as emp_revenue -- Aggregate function for calculating revenue of employee
		from employees
		left join orders on employees.emp_id = orders.emp_id
		group by employees.emp_id, employees.name
),
DeptInfo as (
		select employees.dept_id as dept_id, sum(orders.order_amount) as dept_revenue -- Aggregate Function for calculating revenue of department
		from employees
		left join orders on employees.emp_id = orders.emp_id
		group by employees.dept_id
)
select EmpInfo.emp_id, EmpInfo.name, EmpInfo.dept_id, EmpInfo.emp_revenue, DeptInfo.dept_revenue,
		EmpInfo.emp_revenue * 100/nullif(DeptInfo.dept_revenue, 0) as RevenueContribution -- Formula for contribution
from EmpInfo
inner join DeptInfo on EmpInfo.dept_id = DeptInfo.dept_id
where dept_revenue is not null;


-- this is a combination of distinct,  aggregate function, window function
with EmpInfo as (
		select employees.emp_id as emp_id, employees.name as name, employees.dept_id as dept_id, 
		sum(orders.order_amount) as emp_revenue -- aggregate function
		from employees
		left join orders on employees.emp_id = orders.emp_id
		group by employees.emp_id, employees.name -- group by clause
),
WithDeptInfo as (
		select *, sum(emp_revenue) over(partition by dept_id) as dept_revenue  from EmpInfo) -- Window Function
        -- distinct keyword is also used here
select distinct EmpInfo.emp_id, EmpInfo.name, EmpInfo.dept_id, EmpInfo.emp_revenue * 100/nullif(WithDeptInfo.dept_revenue, 0) as Contribution
from EmpInfo 
inner join WithDeptInfo on EmpInfo.dept_id = WithDeptInfo.dept_id;





-- 5. MAnagers whose subordinates outperforms them
-- Business Insight: Identifies Employees who perform better than their managers in a team

-- using aggregate function, group by and without distinct
with Managerinfo as (
		select employees.emp_id as emp_id, employees.name as name, employees.dept_id as dept_id, 
        sum(orders.order_amount) as revenue, employees.manager_id as manager_id
		from employees 
		left join orders on employees.emp_id = orders.emp_id
		group by employees.emp_id, employees.name
)
select m.dept_id, m.name as manager, m.revenue as revenue, e.name as employee, e.revenue as revenue
from Managerinfo m
join Managerinfo e on m.emp_id = e.manager_id
where m.revenue <= e.revenue;


-- using distinct and window function
with managers as (
	select  distinct employees.emp_id, employees.name, employees.dept_id, 
		sum(orders.order_amount) over(partition by orders.emp_id
									  order by orders.emp_id asc) as revenue,
	    employees.manager_id
	from employees
left join orders on employees.emp_id = orders.emp_id
)
select m.name as manager, m.revenue as manager_revenue, a.emp_id, a.name, a.dept_id, a.revenue
from managers m
join managers a on m.emp_id = a.manager_id
where a.revenue > m.revenue;





